<p>Bolo de trigo simples:</p>
<ul>
    <li>3 colheres de sopa de Margarina Doriana sem Sal</li>
    <li>3 ovos</li>
    <li>2 xícaras de açúcar</li>
    <li>1 xícara de leite</li>
    <li>3 xícaras de farinha de trigo</li>
    <li>1 colher de sopa de fermento em pó</li>
</ul>
<p>Como fazer bolo de trigo:</p>
<p>Preaqueça o forno a 180ºC. Em uma batedeira, bata os ovos com a Margarina Doriana sem Sal e o açúcar até formar um
    creme fofo;<br>
    Adicione o leite e a farinha de trigo aos poucos, intercalando ambos e terminando com a farinha;<br>
    Dispense a batedeira, adicione o fermento mexendo delicadamente e transfira para uma forma de bolo;<br>
    Leve ao forno por 30 minutos;<br>
    Retire do forno e sirva morno ou em temperatura ambiente.
</p>