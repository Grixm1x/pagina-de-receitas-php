<p>Ingredientes:</p>
<ul>
    <li>1 maço de alface-americana</li>
    <li>½ repolho roxo (cerca de 3 xícaras [chá])</li>
    <li>1 cenoura</li>
    <li>1 pepino</li>
    <li>2 colheres (sopa) de açúcar mascavo</li>
    <li>3 colheres (sopa) de shoyu (molho de soja)</li>
    <li>2 colheres (sopa) de vinagre de arroz</li>
    <li>6 colheres (sopa) de óleo</li>
    <li>1 colher (sopa) de óleo de gergelim torrado</li>
    <li>gergelim preto torrado a gosto para servir</li>
</ul>
<p>Modo de preparo:</p>
<P>Corte o maço de alface-americana ao meio, apoie a parte plana na tábua e fatie as metades em tiras de 1 cm. Com um
    fatiador de legumes (ou mandolim), corte o repolho em fatias bem finas; vá passando o repolho pelo mandolim, com
    cuidado para não fatiar o miolo central, que é muito firme. Se preferir, descarte o miolo e fatie fino o repolho com
    a faca.<br>
    Transfira as folhas para uma tigela grande, cubra com água e adicione o bactericida de sua escolha. Deixe de molho
    por 15 minutos. Enquanto isso, prepare os outros ingredientes.<br>
    Lave e seque a cenoura e o pepino. Descasque e passe a cenoura pela parte grossa do ralador. Descarte as pontas e
    corte o pepino ao meio no sentido do comprimento; corte cada metade em 4 tiras e as tiras em fatias de 1 cm.<br>
    Para fazer o molho: num pote de vidro com tampa, coloque o açúcar, o shoyu, o vinagre, o óleo e o óleo de gergelim;
    tampe e chacoalhe bem para misturar.<br>
    Em vez de escorrer, retire as folhas da água — assim as sujeirinhas ficam no fundo da tigela. Enxágue e seque bem as
    folhas na centrífuga de saladas.<br>
    Transfira as folhas para uma saladeira (ou tigela grande), junte a cenoura e o pepino e misture bem. Salpique o
    gergelim e sirva a seguir com o molho.</P>