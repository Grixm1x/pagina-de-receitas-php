<h1>Receita de Pizza</h1>
<p>Ingredientes para a Massa:</p>
<ul>
    <li>500 g de farinha de trigo (aproximadamente 3 xícaras e meia)</li>
    <li>1 colher de chá de sal</li>
    <li>1 colher de sopa de açúcar</li>
    <li>10 g de fermento biológico seco (1 envelope)</li>
    <li>300 ml de água morna</li>
    <li>2 colheres de sopa de azeite de oliva (ou óleo)</li>
    <li>1 colher de chá de orégano seco (opcional, para dar um toque especial)</li>
</ul>
<p>Modo de Preparo:</p>
<p>Preparando a mistura de fermento: Em uma tigela pequena, adicione a água morna (não muito quente, para não matar o
    fermento). Coloque o açúcar e o fermento biológico seco e mexa até dissolver. Deixe descansar por 5 a 10 minutos,
    até que o fermento comece a formar uma espuma na superfície da água. Isso indica que o fermento está ativado e
    pronto para ser usado.
    Misturando os ingredientes secos: Em uma tigela maior, coloque a farinha de trigo e o sal. Se optar por adicionar
    orégano à massa, coloque-o junto com a farinha para garantir que o sabor seja incorporado uniformemente. <br>
    Incorporando os líquidos à farinha: Faça um buraco no centro da farinha e despeje a mistura de fermento já espumada,
    juntamente com o azeite de oliva. Misture com uma colher ou com as mãos, incorporando os ingredientes secos e
    líquidos até que se forme uma massa.<br>
    Sovando a massa: Transfira a massa para uma superfície limpa e enfarinhada e comece a sovar por cerca de 10 minutos.
    A massa deve ficar lisa, elástica e levemente pegajosa, mas não grudando nas mãos. Se necessário, adicione um pouco
    mais de farinha, mas sem exagerar, pois a massa pode ficar seca. <br>
    Deixando a massa descansar: Coloque a massa de volta na tigela e cubra com um pano limpo ou filme plástico. Deixe
    descansar em um lugar morno por cerca de 1 hora, ou até que a massa dobre de tamanho. Esse processo é fundamental
    para que o fermento atue e a massa cresça, tornando-se leve e macia.<br>
    Modelando a massa: Após o descanso, retire o ar da massa e divida-a em duas porções, caso queira fazer duas pizzas
    médias, ou use toda a massa para uma pizza grande. Abra a massa com as mãos ou um rolo de massa, no tamanho e
    espessura que preferir, formando o disco de pizza. Se a massa estiver muito elástica e difícil de esticar, deixe
    descansar por mais alguns minutos antes de tentar novamente.<br>
    Pré-assando a massa (opcional): Para uma crosta mais crocante, você pode pré-assar a massa antes de adicionar o
    molho e a cobertura. Para isso, coloque a massa em uma assadeira untada com azeite ou em uma pedra para pizza e leve
    ao forno pré-aquecido a 220°C por cerca de 5 a 7 minutos, até que ela comece a dourar levemente. Esse passo evita
    que a massa fique mole após adicionar o molho.<br>
    Montando a pizza: Depois de pré-assar (ou não, se preferir uma massa mais macia), adicione o molho de tomate, o
    queijo e suas coberturas preferidas (como tomate, cogumelos, presunto, peperoni, etc.). Lembre-se de não
    sobrecarregar a pizza com muitas coberturas para garantir que a massa cozinhe de maneira uniforme.<br>
    Assando a pizza: Coloque a pizza montada no forno pré-aquecido a 220°C e asse por 10 a 15 minutos ou até que as
    bordas da massa estejam douradas e o queijo tenha derretido completamente. O tempo de assamento pode variar de
    acordo com o forno, por isso, fique de olho na pizza para evitar que ela queime.<br>
    Servindo: Retire a pizza do forno, corte em fatias e sirva imediatamente. Para um toque final, você pode adicionar
    algumas folhas de manjericão fresco ou um fio de azeite por cima da pizza.</p>